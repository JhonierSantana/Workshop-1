const a = 4;
const b = 7;
const c = 2 * b;
const x = a;
const y = 3 * x;
const u = 6;
const w = 10;
const z = 2 * w;

// A.
resultado = a / (b * c);
console.log("A.", resultado);

// B.
resultado2 = Math.pow(a, 2) + Math.pow(b, 92); // a ** 2 + b ** 92
console.log("B.", resultado2);

// C.
resultado3 = x + y / (u + w / a);
console.log("C.", resultado3);

// D.
resultado4 = (x / y) * (z + w);
console.log("D.", resultado4);
